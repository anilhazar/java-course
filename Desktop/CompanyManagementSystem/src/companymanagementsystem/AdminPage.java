    private void adminInsertButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_adminInsertButtonActionPerformed
        try {
            String selectedTable = String.valueOf(adminCombobox.getSelectedItem());
            String[] columnNames = connections.getColumnNames(selectedTable);

            String[] values = new String[3]; // veya gerekli sayýda elemanlý bir dizi
            values[0] = columnArea1.getText();
            values[1] = columnArea2.getText();
            values[2] = columnArea3.getText();

            connections.insertData(selectedTable, columnNames, values);

            adminErrorMessage.setText(" NEW " + selectedTable + " SUCCESSFULLY INSERTED TO THE TABLE !");
            columnArea1.setText("");
            columnArea2.setText("");
            columnArea3.setText("");

            updateTable(selectedTable);

        } catch (Exception ex) {
            adminErrorMessage.setText("INSERT ATTEMPT FAILED !!!");
        }
    }//GEN-LAST:event_adminInsertButtonActionPerformed

    private void adminUpdateButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_adminUpdateButtonActionPerformed

        adminErrorMessage.setText("");
        int selectedRowIndex = jTable1.getSelectedRow();
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        Object primaryKeyValue = jTable1.getValueAt(selectedRowIndex, 0);
        String primaryKeyValueAsString = primaryKeyValue.toString();
        // Seçilen satýrýn indexini al

        if (selectedRowIndex == -1) {
            if (adminTable.getRowCount() == 0) {
                adminErrorMessage.setText("Table is Empty...");
            } else {
                adminErrorMessage.setText("Please click on a row to Update");
            }

        } else {

            try {
                adminErrorMessage.setText("");

                if (selectedRowIndex == -1) {
                    if (adminTable.getRowCount() == 0) {
                        adminErrorMessage.setText("Table is Empty...");
                    } else {
                        adminErrorMessage.setText("Please click on a row to Update");
                    }
                } else {
                    String tableName = String.valueOf(adminCombobox.getSelectedItem());

                    String newValue1 = columnArea1.getText();
                    String newValue2 = columnArea2.getText();
                    String newValue3 = columnArea3.getText();

                    // Seçilen satýrýn özgün deðerleri
                    model.getValueAt(selectedRowIndex, 0).toString();

                    // Yeni deðerleri güncelleme metoduna iletiyoruz
                    String[] columnNames = {model.getColumnName(0), model.getColumnName(1), model.getColumnName(2)}; // Sütun isimleri
                    String[] newValues = {newValue1, newValue2, newValue3}; // Yeni deðerler
                    String primaryKeyColumnName = jTable1.getColumnName(0); // Primary key sütun adý

                    connections.updateData(tableName, columnNames, newValues, primaryKeyColumnName, primaryKeyValueAsString);

                    // Tabloyu güncelle
                    updateTable(tableName);

                    // Text field'larý sýfýrla
                    columnArea1.setText("");
                    columnArea2.setText("");
                    columnArea3.setText("");
                }
            } catch (Exception exx) {
                System.out.println(exx);
            }
        }
    }//GEN-LAST:event_adminUpdateButtonActionPerformed

    private void adminDeleteButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_adminDeleteButtonActionPerformed
        String primaryKeyColumnName = jTable1.getColumnName(0);
        String tableName = String.valueOf(adminCombobox.getSelectedItem());
        int selectedRowIndex = jTable1.getSelectedRow();
        Object primaryKeyValue = jTable1.getValueAt(selectedRowIndex, 0);
        String primaryKeyValueAsString = primaryKeyValue.toString();
        if (selectedRowIndex != -1) {

            connections.deleteRow(tableName, primaryKeyColumnName, primaryKeyValueAsString);

            // Swing tablonuzu güncellemeyi unutmayýn
            updateTable(tableName);
        } else {
            // Kullanýcý bir satýr seçmediðinde bir hata mesajý gösterebilirsiniz.
        }

    }//GEN-LAST:event_adminDeleteButtonActionPerformed

