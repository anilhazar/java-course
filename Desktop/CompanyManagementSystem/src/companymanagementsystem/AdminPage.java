    private void adminComboboxActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_adminComboboxActionPerformed
        try {
            String tableName = String.valueOf(adminCombobox.getSelectedItem());

            String[] columnNames = connections.getColumnNames(tableName);
            List<Object[]> rowDataList = connections.fetchRowObjects(tableName); // Verileri çek

            DefaultTableModel model = new DefaultTableModel();
            for (String columnName : columnNames) {
                model.addColumn(columnName); // Her bir sütun ismini modele ekle
            }
            adminColumn1.setText(columnNames[0]);
            adminColumn2.setText(columnNames[1]);
            adminColumn3.setText(columnNames[2]);

            for (Object[] rowData : rowDataList) {
                model.addRow(rowData); // Her bir satýrý modele ekle
            }

            jTable1.setModel(model); // Tabloya modeli ekle
            jScrollPane2.setViewportView(jTable1);

        } catch (SQLException e) {
            e.printStackTrace();
        }

    }//GEN-LAST:event_adminComboboxActionPerformed

